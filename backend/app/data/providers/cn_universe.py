def _dedup(seq: list[tuple[str, str]]) -> list[tuple[str, str]]:
    """按 symbol 去重,保留首次出现的 (symbol, name) 对。"""
    seen: set[str] = set()
    out: list[tuple[str, str]] = []
    for sym, name in seq:
        if sym not in seen:
            seen.add(sym)
            out.append((sym, name))
    return out


